char *ft_strrchr(const char *s, int c)
{
	int ret;
	int i;

	i = 0; 
	ret = 0;
	while (s[i] != 0)
	{
		if (s[i] == c)
			ret = i;
		i++;
	}
	if (c == 0)
		return ((char *)&s[i]);
	else if (ret != 0)
		return ((char *)&s[ret]);
	return (0);
}
